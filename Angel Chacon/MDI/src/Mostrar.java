    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        try{
            
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/prueba", "root", "compromiso");
            PreparedStatement pst = cn.prepareStatement("select * from maestros where idmaestro = ?");
            pst.setString(1, txtBuscar.getText().trim());
            
            ResultSet rs = pst.executeQuery();
            
            double S = 0, B = 0, E = 0, T = 0, I = 0, M = 0;
            
            if(rs.next()){
                txtNombrem.setText(rs.getString("nombremaestro"));
                txtSueldom.setText(rs.getString("sueldo"));
                txtBonificacionm.setText(rs.getString("bonificacion"));
                txtExtram.setText(rs.getString("extraordinario"));
                
                S = Double.parseDouble(txtSueldom.getText().trim());
                B = Double.parseDouble(txtBonificacionm.getText().trim());
                E = Double.parseDouble(txtExtram.getText().trim());
                
                T = (S + B + E);
                
                
                
                
                PreparedStatement pst2 = cn.prepareStatement("select * from conceptos");
                
                ResultSet rs2 = pst2.executeQuery();
                
                
                
                if(rs2.next()){
                    
                    txtMinc.setText(rs2.getString("cantidadmin"));
                
                M = Double.parseDouble(rs2.getString("cantidadmin"));
                    
                    if(T >= M){
                    
                    txtNombrec.setText(rs2.getString("nombreconcepto"));
                    txtMaxc.setText(rs2.getString("cantidadmax"));
                    txtMinc.setText(rs2.getString("cantidadmin"));
                    txtAplicacionc.setText(rs2.getString("aplicacion"));
                
                    I = Double.parseDouble(txtAplicacionc.getText().trim());
                    
                    T = (S + B + E) * I;
                    
                    }
                    
                }
                
                
                txtTotalm.setText("" + T);

                
            } else {
                JOptionPane.showMessageDialog(null, "Alumno no registrado.");
            }
            
        }catch (Exception e){
            
        }
        
        
        
    }//GEN-LAST:event_btnBuscarActionPerformed


